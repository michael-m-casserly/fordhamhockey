function addOption(selectbox,text,value,isSelected )	{
	var optn = document.createElement("OPTION");
	optn.text = text;
	optn.value = value;
	if(isSelected) {
		optn.selected = isSelected ;
	}	
	selectbox.options.add(optn);
}

function removeAllOptions(selectbox) {
	var i;
	var selectboxEl = document.getElementById(selectbox);
	var selectedProd = selectboxEl.value;
	for(i=selectboxEl.options.length-1;i>=0;i--){
		selectboxEl.remove(i);
	}
	return selectedProd ;
}


function updateSelectBox(selectBox, optionsArry, selectedValue) {
	var isSelected = false;	
	for(j = 0 ; j < optionsArry.length; j++) {
		isSelected = false;
		//Need to add if condition to filter the Products Based on the Type selected.
		if(optionsArry[j][2] == selectedValue) {
			isSelected = true;
		}
		addOption(selectBox, optionsArry[j][1], optionsArry[j][0],isSelected);
	}
}

